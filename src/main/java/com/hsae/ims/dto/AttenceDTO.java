package com.hsae.ims.dto;

public class AttenceDTO {

	private String id;
	private String personNo;
	private String personName;
	private String brushDate;
	private String attenceTimes;
	private String userId;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPersonNo() {
		return personNo;
	}
	public void setPersonNo(String personNo) {
		this.personNo = personNo;
	}
	public String getPersonName() {
		return personName;
	}
	public void setPersonName(String personName) {
		this.personName = personName;
	}
	public String getBrushDate() {
		return brushDate;
	}
	public void setBrushDate(String brushDate) {
		this.brushDate = brushDate;
	}
	public String getAttenceTimes() {
		return attenceTimes;
	}
	public void setAttenceTimes(String attenceTimes) {
		this.attenceTimes = attenceTimes;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	
}
