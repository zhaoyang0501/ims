package com.hsae.ims.dto;

public class AttenceCountStateDTO {
	
	private String index;
	private String userName;
	private String overtimeCount;
	private String dayoffCount;
	private String overtimeHours;
	private String overtimeDate;
	private String projectName;
	public String getIndex() {
		return index;
	}
	public void setIndex(String index) {
		this.index = index;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getOvertimeCount() {
		return overtimeCount;
	}
	public void setOvertimeCount(String overtimeCount) {
		this.overtimeCount = overtimeCount;
	}
	public String getDayoffCount() {
		return dayoffCount;
	}
	public void setDayoffCount(String dayoffCount) {
		this.dayoffCount = dayoffCount;
	}
	public String getOvertimeHours() {
		return overtimeHours;
	}
	public void setOvertimeHours(String overtimeHours) {
		this.overtimeHours = overtimeHours;
	}
	public String getOvertimeDate() {
		return overtimeDate;
	}
	public void setOvertimeDate(String overtimeDate) {
		this.overtimeDate = overtimeDate;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	
}
